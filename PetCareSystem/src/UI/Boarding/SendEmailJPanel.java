    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        // TODO add your handling code here:
        String name = fieldName.getText().trim();
        String email = fieldEmail.getText().trim();
        String message = txtMessage.getText().trim();

        // 验证输入
        if (name.isEmpty() || email.isEmpty() || message.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "Please fill in all fields (Name, Email, Message).", 
                "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 验证邮箱格式（简单验证）
        if (!email.contains("@") || !email.contains(".")) {
            JOptionPane.showMessageDialog(this, 
                "Please enter a valid email address.", 
                "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 模拟发送邮件（实际项目中需要集成邮件服务）
        // 这里只是显示成功消息
        JOptionPane.showMessageDialog(this, 
            "Email sent successfully!\n\n" +
            "To: " + name + " <" + email + ">\n" +
            "Message: " + message.substring(0, Math.min(50, message.length())) + "...",
            "Email Sent", JOptionPane.INFORMATION_MESSAGE);

        // 更新请求状态（如果有）
        if (selectedRequest != null) {
            selectedRequest.setStatus("Customer Notified via Email");
        }

        // 清空表单
        txtMessage.setText("");

        // 返回上一页
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_btnSendActionPerformed


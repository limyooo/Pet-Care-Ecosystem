// ⭐ 加载请求信息（预填充收件人信息）
    public void loadRequestDetails(HealthCareCheckRequest request) {
        this.selectedRequest = request;
        if (request == null) return;

        Pet pet = request.getPet();
        PetOwner owner = (pet != null) ? pet.getPetOwner() : null;

        // 预填充收件人信息
        if (owner != null) {
            fieldName.setText(owner.getOwnerName() != null ? owner.getOwnerName() : "");
            fieldEmail.setText(owner.getEmail() != null ? owner.getEmail() : "");
        }

        // 预填充邮件模板
        String petName = (pet != null) ? pet.getPetName() : "your pet";
        String symptom = (request.getSymptom() != null) ? request.getSymptom() : "some symptoms";
        
        String template = "Dear " + (owner != null ? owner.getOwnerName() : "Pet Owner") + ",\n\n" +
                "We are writing to inform you about " + petName + "'s health status.\n\n" +
                "Observed symptom: " + symptom + "\n\n" +
                "Please contact us at your earliest convenience to discuss the next steps.\n\n" +
                "Best regards,\n" +
                "Pet Boarding Customer Service Team";
        
        txtMessage.setText(template);
    }
